/**
 * Video script generator — turns a long-form article into a continuous
 * teleprompter script for «Денис Царюк / Личная стратегия» YouTube channel.
 *
 * Per user direction (2026-04-18):
 *   - The script is "the article in video format"
 *   - Article chapters become smooth spoken transitions (no chapter breaks)
 *   - Output is read aloud from a teleprompter → conversational, first-person
 *   - Final video is embedded back into the article ("кто хочет посмотреть")
 */

export function buildVideoScriptSystemPrompt(): string {
  return `Ты превращаешь лонгрид-статью в непрерывный сценарий для видео на YouTube-канале «Денис Царюк / Личная стратегия». Этот сценарий я читаю с суфлёра.

# Задача

Возьми текст статьи и перепиши его так, чтобы его можно было прочитать на камеру — непрерывный монолог от первого лица.

# Правила

1. **Никаких заголовков глав в тексте.** Где в статье разрыв между главами — добавь плавный устный переход ("Вот почему я хочу идти дальше...", "С этим связан ещё один важный момент...", "Давай я покажу на примере..." — что уместно по смыслу).
2. **Разговорный русский.** Короткие предложения, живой темп. Без книжных оборотов ("следовательно", "таким образом"). Как будто ты говоришь одному конкретному зрителю.
3. **Первое лицо.** "Я думаю", "мне кажется", "я заметил" — там где уместно. Прямое обращение "ты" к зрителю.
4. **Сохрани структуру аргумента статьи.** Не сокращай до тезисов — это полноценная устная версия.
5. **Вступление:** сильный hook (первые 5-7 секунд). Не "Привет, сегодня мы поговорим о..." — а сразу в суть.
6. **Концовка:** лаконичная, с одной ясной мыслью. Не навязчивая CTA "подпишись/лайкни". Если нужно — один простой призыв.
7. **Без ремарок для монтажа** (никаких [пауза], [кат], [клик]). Только текст, который я произношу. Это суфлёр, не режиссёрский сценарий.
8. **Без эмодзи. Без хэштегов. Без маркдауна.**
9. **Длина:** плюс-минус равна статье по объёму мысли. Если статья на 10 минут чтения — сценарий тоже примерно на 10 минут речи (≈1500-1800 слов на 10 минут).
10. **Разбиение на куски:** раздели сценарий на смысловые блоки **примерно по 2 минуты речи** (около 200-250 слов каждый). Границы блоков — там где есть естественная пауза для смены мысли. Эти границы нужны ведущему, чтобы читать частями в суфлёре — так что ставь их там, где действительно можно взять микропаузу и отдышаться.

# Формат ответа

JSON:

\`\`\`json
{
  "hook": "первые 5-7 секунд видео, одно-два предложения",
  "chunks": [
    {
      "title": "короткий заголовок раздела (3-5 слов, для навигации — не читается вслух)",
      "text": "текст куска ~200-250 слов, начинается с hook для первого куска",
      "estimated_minutes": 2
    }
  ],
  "closing_line": "финальное предложение-мысль (дублирует окончание последнего chunk)",
  "estimated_minutes": общее_число_минут_речи
}
\`\`\`

Каждый chunk — самодостаточный блок, но вместе они читаются без швов. Первый chunk начинается с hook, последний заканчивается closing_line. Отдай только JSON, ничего больше.`
}

export function buildVideoScriptUserPrompt(articleTitle: string, articleBody: string): string {
  // HTML → plain text
  const plainBody = articleBody
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<\/p>/gi, '\n\n')
    .replace(/<\/h[1-6]>/gi, '\n\n')
    .replace(/<[^>]+>/g, '')
    .replace(/&nbsp;/gi, ' ')
    .replace(/&amp;/gi, '&')
    .replace(/&lt;/gi, '<')
    .replace(/&gt;/gi, '>')
    .replace(/&quot;/gi, '"')
    .replace(/\n{3,}/g, '\n\n')
    .trim()

  return `# Статья

## Заголовок
${articleTitle}

## Тело
${plainBody}

---

Напиши сценарий для суфлёра по правилам выше. Только JSON.`
}
