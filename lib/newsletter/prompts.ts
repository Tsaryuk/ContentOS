export function buildChatUserPrompt(opts: {
  message: string
  currentHtml?: string
  selectedText?: string
}): string {
  let prompt = opts.message

  if (opts.selectedText) {
    prompt += `\n\n--- Выделенный фрагмент ---\n${opts.selectedText}`
  }

  if (opts.currentHtml) {
    const textOnly = opts.currentHtml
      .replace(/<[^>]+>/g, '')
      .replace(/\s+/g, ' ')
      .trim()
    if (textOnly.length > 0) {
      prompt += `\n\n--- Текущий текст письма ---\n${textOnly.slice(0, 3000)}`
    }
  }

  return prompt
}
